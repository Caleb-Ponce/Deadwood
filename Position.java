public class Position{
    public String name;
    public String rank;
    public String[] area;
    public String line;
    public boolean onCard;

    public Position(String name, String rank, String[] area, String line, boolean onCard) {
      this.name = name;
      this.rank = rank;
      this.area = area;
      this.line = line;
      this.onCard = onCard;
    }
}
